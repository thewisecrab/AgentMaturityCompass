"""AMC Product — Agent Scaffolding CLI (Feature 6).

Generates project skeletons for AMC-compatible agents, including:
- Directory structure with amc/ package
- Policy manifest (amc_policy.json)
- Tool wrapper stubs
- Logging setup
- Test scaffolding
- Deploy script skeleton

The generator returns file paths and content as strings so it can be used
headlessly (programmatically or via CLI) without touching the filesystem
unless explicitly requested.

Revenue path: reduces builder friction → faster ecosystem growth → more
deployments on AMC platform (Lever C → A pipeline).
"""
from __future__ import annotations

import json
import textwrap
from dataclasses import dataclass, field
from enum import Enum
from pathlib import Path
from typing import Any

import structlog

log = structlog.get_logger(__name__)


class AgentTemplate(str, Enum):
    BASIC = "basic"                # Simple single-tool agent
    MULTI_TOOL = "multi_tool"     # Multiple tool connectors
    BROWSER_AUTOMATION = "browser_automation"  # Browser-based workflow
    DATA_PIPELINE = "data_pipeline"  # Data extract-transform-load
    APPROVAL_FLOW = "approval_flow"  # Human-in-the-loop workflow


@dataclass
class ScaffoldFile:
    """A single file to be generated."""
    path: str          # relative path within project
    content: str       # file content
    is_binary: bool = False

    def write_to(self, base: Path) -> Path:
        target = base / self.path
        target.parent.mkdir(parents=True, exist_ok=True)
        target.write_text(self.content, encoding="utf-8")
        return target


@dataclass
class ScaffoldProject:
    """Collection of generated files forming a complete agent project."""
    agent_name: str
    template: AgentTemplate
    files: list[ScaffoldFile] = field(default_factory=list)
    description: str = ""

    @property
    def file_count(self) -> int:
        return len(self.files)

    @property
    def file_paths(self) -> list[str]:
        return [f.path for f in self.files]

    def write_to_disk(self, output_dir: str | Path) -> Path:
        """Write all scaffold files to disk. Returns project root."""
        base = Path(output_dir) / self.agent_name.replace(" ", "_").lower()
        for f in self.files:
            f.write_to(base)
        log.info("scaffolding.written", agent=self.agent_name, path=str(base), files=len(self.files))
        return base


