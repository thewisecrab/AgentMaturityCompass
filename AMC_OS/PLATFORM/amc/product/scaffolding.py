class AgentScaffolder:
    """Generates AMC-compatible agent project files."""

    # Default tools per template
    _DEFAULT_TOOLS: dict[AgentTemplate, list[str]] = {
        AgentTemplate.BASIC: ["search", "summarize"],
        AgentTemplate.MULTI_TOOL: ["search", "summarize", "email", "calendar"],
        AgentTemplate.BROWSER_AUTOMATION: ["navigate", "extract", "click"],
        AgentTemplate.DATA_PIPELINE: ["fetch_data", "transform", "load"],
        AgentTemplate.APPROVAL_FLOW: ["draft", "approve", "send"],
    }

    def generate(
        self,
        agent_name: str,
        template: AgentTemplate = AgentTemplate.BASIC,
        tools: list[str] | None = None,
        description: str = "",
    ) -> ScaffoldProject:
        """Generate a complete scaffold project and return it."""
        slug = agent_name.lower().replace(" ", "_")
        selected_tools = tools or self._DEFAULT_TOOLS.get(template, ["example"])

        project = ScaffoldProject(
            agent_name=slug,
            template=template,
            description=description or f"AMC agent: {agent_name} ({template.value} template)",
        )

        # Root files
        project.files.extend([
            ScaffoldFile("README.md", _readme(agent_name, template, selected_tools)),
            ScaffoldFile("requirements.txt", _requirements_txt(template)),
            ScaffoldFile("amc_policy.json", _policy_manifest(agent_name, selected_tools)),
            ScaffoldFile("deploy.sh", _deploy_sh(agent_name)),
            ScaffoldFile("pyproject.toml", self._pyproject_toml(slug)),
        ])

        # Agent package
        project.files.extend([
            ScaffoldFile(f"{slug}/__init__.py", _init_py(slug)),
            ScaffoldFile(f"{slug}/main.py", _main_py(agent_name, selected_tools, template)),
            ScaffoldFile(f"{slug}/config.py", _config_py(agent_name)),
            ScaffoldFile(f"{slug}/tools/__init__.py", _init_py("tools")),
        ])

        # Tool stubs
        for tool in selected_tools:
            project.files.append(
                ScaffoldFile(f"{slug}/tools/{tool}.py", _tool_stub(tool))
            )

        # Tests
        project.files.extend([
            ScaffoldFile("tests/__init__.py", ""),
            ScaffoldFile("tests/test_tools.py", _test_file(agent_name, selected_tools)),
        ])

        log.info(
            "scaffolding.generated",
            agent=agent_name,
            template=template.value,
            tools=selected_tools,
            file_count=len(project.files),
        )
        return project

    @staticmethod
    def _pyproject_toml(slug: str) -> str:
        return textwrap.dedent(f"""\
            [build-system]
            requires = ["setuptools>=68"]
            build-backend = "setuptools.backends.legacy:build"

            [project]
            name = "{slug}"
            version = "0.1.0"
            description = "AMC-compatible agent"
            requires-python = ">=3.11"
            dependencies = []

            [tool.pytest.ini_options]
            asyncio_mode = "auto"
        """)


