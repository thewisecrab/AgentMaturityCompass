def _policy_manifest(agent_name: str, tools: list[str]) -> str:
    """Generate amc_policy.json compatible with the AMC enforce layer."""
    manifest = {
        "manifest_version": "1.0",
        "agent_name": agent_name,
        "description": f"AMC policy manifest for {agent_name}",
        "allow_list": [
            {
                "tool": t,
                "categories": ["read_only"],
                "require_trust": "trusted",
            }
            for t in tools
        ],
        "deny_list": ["exec", "eval", "subprocess"],
        "session_trust_default": "untrusted",
        "rate_limits": {
            "max_tool_calls_per_minute": 60,
            "max_model_calls_per_minute": 20,
        },
        "audit": {
            "receipt_level": "full",
            "pii_redact": True,
        },
    }
    return json.dumps(manifest, indent=2)


def _main_py(agent_name: str, tools: list[str], template: AgentTemplate) -> str:
    tool_imports = "\n".join(f"from .tools.{t} import {t}_tool" for t in tools)
    tool_registry = ", ".join(f'"{t}": {t}_tool' for t in tools)
    return textwrap.dedent(f"""\
        \"\"\"
        {agent_name} — AMC-compatible agent entry point.
        Generated by AMC scaffolding CLI (feature-6).
        \"\"\"
        from __future__ import annotations

        import asyncio
        import structlog

        from amc.watch.w1_receipts import get_ledger
        from amc.core.models import SessionTrust, ToolCategory
        from .config import AgentConfig

        {tool_imports}

        log = structlog.get_logger(__name__)

        TOOL_REGISTRY = {{
            {tool_registry}
        }}


        async def run(task: dict, config: AgentConfig | None = None) -> dict:
            \"\"\"Execute a task using this agent.\"\"\"
            cfg = config or AgentConfig()
            log.info("{agent_name}.run", task_keys=list(task.keys()))

            tool_name = task.get("tool")
            if tool_name not in TOOL_REGISTRY:
                return {{"error": f"Unknown tool: {{tool_name}}"}}

            tool_fn = TOOL_REGISTRY[tool_name]
            result = await tool_fn(task.get("params", {{}}), cfg)

            # Record receipt for auditability
            await get_ledger().record(
                session_id=task.get("session_id", "local"),
                sender_id=task.get("actor", "agent"),
                trust_level=SessionTrust.TRUSTED,
                tool_name=tool_name,
                tool_category=ToolCategory.READ_ONLY,
                parameters_redacted=task.get("params", {{}}),
                outcome_summary=str(result)[:200],
            )

            return result


        if __name__ == "__main__":
            asyncio.run(run({{"tool": "{tools[0] if tools else 'example'}", "params": {{}}}}))
    """)


def _config_py(agent_name: str) -> str:
    return textwrap.dedent(f"""\
        \"\"\"Configuration for {agent_name}.\"\"\"
        from __future__ import annotations
        from dataclasses import dataclass, field
        from typing import Any


        @dataclass
        class AgentConfig:
            \"\"\"Configurable agent settings (injected at runtime).\"\"\"
            max_retries: int = 3
            timeout_seconds: int = 30
            dry_run: bool = False
            metadata: dict[str, Any] = field(default_factory=dict)
    """)


def _tool_stub(tool_name: str) -> str:
    return textwrap.dedent(f"""\
        \"\"\"Tool stub: {tool_name}.

        Replace this stub with a real implementation.
        Keep the async signature and return a serializable dict.
        \"\"\"
        from __future__ import annotations
        from typing import Any
        import structlog

        log = structlog.get_logger(__name__)


        async def {tool_name}_tool(params: dict[str, Any], config: Any) -> dict[str, Any]:
            \"\"\"Execute the {tool_name} tool with the given params.\"\"\"
            log.info("{tool_name}.called", params_keys=list(params.keys()))

            if config.dry_run:
                return {{"dry_run": True, "tool": "{tool_name}", "params": params}}

            # TODO: implement real {tool_name} logic here
            raise NotImplementedError("{tool_name} tool not yet implemented")
    """)


def _test_file(agent_name: str, tools: list[str]) -> str:
    imports = "\n".join(f"from {agent_name.lower()}.tools.{t} import {t}_tool" for t in tools)
    test_cases = "\n\n".join(
        textwrap.dedent(f"""\
            @pytest.mark.asyncio
            async def test_{t}_tool_dry_run() -> None:
                config = AgentConfig(dry_run=True)
                result = await {t}_tool({{}}, config)
                assert result.get("dry_run") is True
        """)
        for t in tools
    )
    return textwrap.dedent(f"""\
        \"\"\"Tests for {agent_name} agent tools.\"\"\"
        import pytest
        {imports}
        from {agent_name.lower()}.config import AgentConfig


        {test_cases}
    """)


def _requirements_txt(template: AgentTemplate) -> str:
    base = ["fastapi>=0.115.0", "pydantic>=2.0.0", "structlog>=24.0.0", "httpx>=0.27.0"]
    if template == AgentTemplate.BROWSER_AUTOMATION:
        base.append("playwright>=1.44.0")
    if template == AgentTemplate.DATA_PIPELINE:
        base.extend(["pandas>=2.0.0", "sqlalchemy>=2.0.0"])
    return "\n".join(base) + "\n"


def _init_py(module_name: str) -> str:
    return f'"""AMC agent module: {module_name}."""\n'


def _readme(agent_name: str, template: AgentTemplate, tools: list[str]) -> str:
    return textwrap.dedent(f"""\
        # {agent_name}

        > Generated by AMC Agent Scaffolding CLI — template: `{template.value}`

        ## Overview

        This agent implements the **{template.value}** workflow pattern.
        It is compatible with the AMC enforce/watch/receipt stack out of the box.

        ## Tools

        {chr(10).join(f'- `{t}`' for t in tools)}

        ## Quick Start

        ```bash
        python -m pip install -r requirements.txt
        python -m {agent_name.lower()}.main
        ```

        ## Policy

        Edit `amc_policy.json` to configure trust levels, rate limits,
        and tool allow/deny lists before deploying to a production AMC runtime.

        ## Testing

        ```bash
        pytest tests/ -v
        ```

        ## AMC Fit

        This project was scaffolded to run inside the AMC runtime.
        Ensure `AMC_POLICY_FILE` points to `amc_policy.json` and
        `AMC_ENV` is set to `production` before deployment.
    """)


def _deploy_sh(agent_name: str) -> str:
    slug = agent_name.lower().replace(" ", "_")
    return textwrap.dedent(f"""\
        #!/usr/bin/env bash
        # Deploy script for {agent_name}
        set -euo pipefail

        AGENT_NAME="{slug}"
        AMC_ENV="${{AMC_ENV:-staging}}"
        AMC_TENANT_ID="${{AMC_TENANT_ID:-local}}"

        echo "[deploy] Deploying $AGENT_NAME to $AMC_ENV (tenant=$AMC_TENANT_ID)"

        # Validate policy manifest
        python -c "import json; json.load(open('amc_policy.json'))" && echo "[deploy] Policy manifest OK"

        # Run tests
        python -m pytest tests/ -q && echo "[deploy] Tests passed"

        # Package
        python -m build --wheel --outdir dist/

        echo "[deploy] Done. Upload dist/ to AMC deployment endpoint."
    """)


